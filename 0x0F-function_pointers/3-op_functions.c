#include "3-calc.h"

/**
 * op_add - adds a to b
 * @a: an integer
 * @b: an integer
 *
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts a from b
 * @a: an integer
 * @b: an integer
 *
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies a with b
 * @a: an integer
 * @b: an integer
 *
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides a by b
 * @a: an integer
 * @b: an integer
 *
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - the modulo of a and b
 * @a: an integer
 * @b: an integer
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
